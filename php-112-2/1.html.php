        <title>html+php</title>
    </head>
    <body>
        <?php 
            // 使用 PHP 輸出 "Hello world!" 到網頁上
            echo "Hello world!"; 
        ?> 

        <!-- 顯示一個 HTML 的 h1 標題 -->
        <h1>html</h1> 

        <?php 
            // 再次使用 PHP 輸出 "Hello world!"
            echo "Hello world!"; 
        ?> 

        <!-- 再次顯示一個 HTML 的 h1 標題 -->
        <h1>html</h1>

        <?php 
            // 第三次輸出 "Hello world!"
            echo "Hello world!"; 
        ?> 
    </body>
